-- Fonction pour calculer le temps d'attente estimé
CREATE OR REPLACE FUNCTION calculer_temps_attente_estime()
RETURNS TRIGGER AS $$
DECLARE
    position_actuelle integer;
    temps_consultation_moyen interval;
    temps_attente interval;
BEGIN
    -- Récupérer la position actuelle dans la file
    SELECT order_position INTO position_actuelle 
    FROM public.waiting_queue 
    WHERE id = NEW.id;
    
    -- Récupérer le temps de consultation moyen du médecin
    SELECT (duree_consultation || ' minutes')::interval INTO temps_consultation_moyen
    FROM public.users 
    WHERE id = NEW.medecin_id;
    
    -- Calculer le temps d'attente estimé
    temps_attente = (position_actuelle - 1) * temps_consultation_moyen;
    
    NEW.temps_attente_estime = temps_attente;
    RETURN NEW;
END;
$$ language 'plpgsql';

-- Trigger pour calculer le temps d'attente estimé
CREATE TRIGGER trigger_calculer_temps_attente_estime 
    BEFORE INSERT OR UPDATE ON public.waiting_queue 
    FOR EACH ROW EXECUTE FUNCTION calculer_temps_attente_estime();

-- Fonction pour créer une notification temps réel
CREATE OR REPLACE FUNCTION creer_notification_realtime(
    p_user_id bigint,
    p_type character varying,
    p_titre character varying,
    p_message text,
    p_data jsonb DEFAULT NULL,
    p_priorite character varying DEFAULT 'normale'
)
RETURNS bigint AS $$
DECLARE
    notification_id bigint;
BEGIN
    INSERT INTO public.notifications_realtime (
        user_id, type_notification, titre, message, data, priorite
    ) VALUES (
        p_user_id, p_type, p_titre, p_message, p_data, p_priorite
    ) RETURNING id INTO notification_id;
    
    RETURN notification_id;
END;
$$ language 'plpgsql';

-- Fonction pour notifier l'arrivée d'un patient (version améliorée)
CREATE OR REPLACE FUNCTION notifier_arrivee_patient_avancee()
RETURNS TRIGGER AS $$
BEGIN
    IF NEW.status = 'present' AND OLD.status != 'present' THEN
        -- Notifier le médecin
        PERFORM creer_notification_realtime(
            NEW.medecin_id,
            'patient_arrive',
            'Patient arrivé',
            'Le patient ' || (SELECT prenom || ' ' || nom FROM public.patients WHERE id = NEW.patient_id) || ' est arrivé en salle d''attente',
            jsonb_build_object(
                'patient_id', NEW.patient_id, 
                'waiting_queue_id', NEW.id,
                'temps_attente_estime', NEW.temps_attente_estime
            ),
            'normale'
        );
        
        -- Marquer que la notification a été envoyée
        NEW.notification_envoyee = true;
        NEW.derniere_notification = now();
    END IF;
    RETURN NEW;
END;
$$ language 'plpgsql';

-- Remplacer l'ancien trigger par le nouveau
DROP TRIGGER IF EXISTS trigger_notifier_arrivee_patient ON public.waiting_queue;
CREATE TRIGGER trigger_notifier_arrivee_patient_avancee 
    AFTER UPDATE ON public.waiting_queue 
    FOR EACH ROW EXECUTE FUNCTION notifier_arrivee_patient_avancee();

-- Fonction pour notifier le scan de documents
CREATE OR REPLACE FUNCTION notifier_scan_document()
RETURNS TRIGGER AS $$
BEGIN
    -- Notifier le médecin que des documents ont été scannés
    PERFORM creer_notification_realtime(
        (SELECT medecin_id FROM public.sessions_scan WHERE id = NEW.id),
        'document_scanne',
        'Document scanné',
        'Nouveau document scanné pour le patient ' || (SELECT prenom || ' ' || nom FROM public.patients WHERE id = NEW.patient_id),
        jsonb_build_object(
            'patient_id', NEW.patient_id,
            'document_id', NEW.id,
            'type_document', NEW.type_document,
            'nom_fichier', NEW.nom_fichier
        ),
        'normale'
    );
    
    RETURN NEW;
END;
$$ language 'plpgsql';

