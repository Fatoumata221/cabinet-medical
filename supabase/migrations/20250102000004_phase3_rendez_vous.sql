-- Fonction pour mettre à jour les statistiques temps réel
CREATE OR REPLACE FUNCTION mettre_a_jour_statistiques_realtime()
RETURNS TRIGGER AS $$
DECLARE
    date_aujourd_hui date := CURRENT_DATE;
    stat_id bigint;
BEGIN
    -- Vérifier si une statistique existe pour aujourd'hui et ce médecin
    SELECT id INTO stat_id 
    FROM public.statistiques_realtime 
    WHERE date_statistique = date_aujourd_hui AND medecin_id = NEW.medecin_id;
    
    IF stat_id IS NULL THEN
        -- Créer une nouvelle entrée de statistiques
        INSERT INTO public.statistiques_realtime (
            date_statistique, medecin_id, nombre_patients_vus
        ) VALUES (
            date_aujourd_hui, NEW.medecin_id, 1
        );
    ELSE
        -- Mettre à jour les statistiques existantes
        UPDATE public.statistiques_realtime 
        SET 
            nombre_patients_vus = nombre_patients_vus + 1,
            updated_at = now()
        WHERE id = stat_id;
    END IF;
    
    RETURN NEW;
END;
$$ language 'plpgsql';

-- Trigger pour mettre à jour les statistiques quand une consultation se termine
CREATE TRIGGER trigger_mettre_a_jour_statistiques 
    AFTER UPDATE ON public.consultations 
    FOR EACH ROW 
    WHEN (NEW.statut = 'terminee' AND OLD.statut != 'terminee')
    EXECUTE FUNCTION mettre_a_jour_statistiques_realtime();

